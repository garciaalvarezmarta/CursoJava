package MyPackage;

public class Recaudacion {
	private long ganancia;
	private int year;
	
	public Recaudacion(long ganancia, int year) {
		super();
		this.ganancia = ganancia;
		this.year = year;
	}

	public long getGanancia() {
		return ganancia;
	}

	public void setGanancia(long ganancia) {
		this.ganancia = ganancia;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}
	
	
	
	

}
